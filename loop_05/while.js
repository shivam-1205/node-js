//while loop //he while statement creates a loop that executes a specified statement as long as the test condition evaluates to true.
// joh while loop hota hai executes karta hai specified statement jab tak condition true nahi hota tab tak execute hota hai

let index=0 
while (index <=10) {
   // console.log(`value of index is ${index}`);
    index=index+2
};

let myarray=["hello","hey","hii","yo"];
let arr=0
while (arr < myarray.length) {
   // console.log(`value is ${myarray[arr]}`);
    arr=arr+1
};

